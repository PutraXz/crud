<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

class Learning_SubjectController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $learning = Learning_subject::latest()->paginate(5);
            return view('learning_subject.index',compact('learning_subject'))
                ->with('i', (request()->input('page', 1) - 1) * 5);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('learning_subject.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'sks' => 'required',
        ]);

        Learning_Subject::create($request->all());

        return redirect()->route('learning_subject.index')
                        ->with('success','learning_subject created successfully.');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return view('learning_subject.show',compact('learning_subject'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return view('learning_subject',compact('learning_subject'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'name' => 'required',
            'sks' => 'required',
        ]);

        $learning->update($request->all());

        return redirect()->route('learning_subject.index')
                        ->with('success','Learning_Subject updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $learning->delete();

        return redirect()->route('learning_subject.index')
                        ->with('success','Learning_Subject deleted successfully');
    }
}
